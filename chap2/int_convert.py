#######################################
# 문자열을 숫자(정수형)로 바꾸기
#######################################

string_a = input("입력A> ")
int_a = int(string_a)

string_b = input("입력B> ")
int_b = int(string_b)

print("문자열 자료: ", string_a + string_b)     # 문자열 연산자 '+' 가 적용되어 문자열을 합침
print("숫자 자료: ", int_a + int_b)             # 숫자 연산자 '+' 가 적용되어 덧셈 연산 수행